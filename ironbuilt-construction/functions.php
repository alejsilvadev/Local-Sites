        'after_title'   => '</h4>',
    ));
}
add_action('widgets_init', 'ironbuilt_widgets_init');

// Allow SVG uploads
function ironbuilt_mime_types($mimes) {
    $mimes['svg'] = 'image/svg+xml';
    return $mimes;
}
add_filter('upload_mimes', 'ironbuilt_mime_types');
