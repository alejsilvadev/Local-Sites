// Theme Setup
function ironbuilt_setup() {
    add_theme_support('title-tag');
    add_theme_support('post-thumbnails');
    add_theme_support('custom-logo');
    add_theme_support('html5', array('search-form', 'comment-form', 'comment-list', 'gallery', 'caption'));

    register_nav_menus(array(
        'primary' => __('Primary Menu', 'ironbuilt'),
        'footer'  => __('Footer Menu', 'ironbuilt'),
    ));
}
add_action('after_setup_theme', 'ironbuilt_setup');

// Enqueue Styles & Scripts
function ironbuilt_scripts() {
    // Google Fonts - Inter
    wp_enqueue_style('google-fonts', 'https://fonts.googleapis.com/css2?family=Inter:wght@300;400;500;600;700;800;900&display=swap', array(), null);

    // Theme stylesheet
    wp_enqueue_style('ironbuilt-style', get_stylesheet_uri(), array(), '1.0.0');

    // Theme JavaScript
    wp_enqueue_script('ironbuilt-script', get_template_directory_uri() . '/assets/js/main.js', array(), '1.0.0', true);
}
add_action('wp_enqueue_scripts', 'ironbuilt_scripts');

